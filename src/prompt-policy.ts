    "Попробуй понять намерение по исходной формулировке. Если данных не хватает или они неоднозначны, задай один короткий уточняющий вопрос.",
    "Если всё понятно, кратко сформулируй нормализованную команду с названием, датой и временем, которую пользователь сможет подтвердить повторной отправкой.",
